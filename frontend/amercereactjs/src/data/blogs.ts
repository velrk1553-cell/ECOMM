export function getBlogPostById(id: string): BlogPost | undefined {
  return blogPostRegistry.find((p) => p.id === id);
}

export function getAdjacentBlogPosts(id: string): {
  prev: BlogPost | null;
  next: BlogPost | null;
} {
  const i = blogPostRegistry.findIndex((p) => p.id === id);
  if (i < 0) return { prev: null, next: null };
  return {
    prev: i > 0 ? blogPostRegistry[i - 1]! : null,
    next: i < blogPostRegistry.length - 1 ? blogPostRegistry[i + 1]! : null,
  };
}
